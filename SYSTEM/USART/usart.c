void USART1_IRQHandler(void)                	//串口1中断服务程序
	{
	u8 Res;
#ifdef OS_TICKS_PER_SEC	 	//如果时钟节拍数定义了,说明要使用ucosII了.
	OSIntEnter();    
#endif
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  
	{
		
		Res =USART_ReceiveData(USART1);//(USART1->DR);	//读取接收到的数据
		if(Res==0xAA&&USART_RX_STA==0)  //判断一次传输的开始
		{
			USART_RX_STA|=0x4000;      //接收状态
			USART_RX_BUF[USART_RX_STA&0X00FF]=Res ;
			
		}
		if(USART_RX_STA)
		{
			USART_RX_BUF[USART_RX_STA&0X00FF]=Res ;
		
			if((USART_RX_STA&0x00FF)==(len+3))
				USART_RX_STA|=0x8000;      //接收完成
		}
		if((USART_RX_STA&0x00FF)==2)
			len=USART_RX_BUF[2];        //获得接收长度
		
		USART_RX_STA++;					 
				
		  		 
   } 
#ifdef OS_TICKS_PER_SEC	 	//如果时钟节拍数定义了,说明要使用ucosII了.
	OSIntExit();  											 
#endif
} 
	
void uart_send_mydata(u8 *data,u8 len)
{
	int i;
	for(i=0;i<len;i++)
	{
		USART_SendData(USART1,data[i]);
		while(USART_GetFlagStatus(USART1,USART_FLAG_TC)!=SET);//等待发送结束
	}
}
	
void uart_receive_mydata(u8 *data,u8 *len)
{
	u8 i=0;
	*len = (USART_RX_STA&0xFF);
	for(i=0;i<*len;i++)
	data[i]=USART_RX_BUF[i];
	
	
}
	

